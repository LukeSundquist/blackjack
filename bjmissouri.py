import random
class Card:
    rank = ''
    suit = ''
    # Initializer. First slot is rank, second slot is suit. Suit will not be important until our stretch goals.
    # Write the rank as 1 character, not as a number. For 2-9, write '2', '3',..., or '9'
    # For Ace, write 'A'
    # For Jack, Queen, King, I would prefer that you write 'J', 'Q', 'K'
    # For 10, I would prefer that you write 'T'
    # Use 'C' for cut card
    def __init__(self, r, s=''):
        self.rank = r
        self.suit = s
    def __str__(self):
        return str(self.rank) + str(self.suit)
    # Use this to get the card's value for blackjack. Just call this function, it's called in Hand
    def val(self):
        match(self.rank):
            # Tens
            case 'T'|'J'|'Q'|'K':
                return 10
            # Ace is yielded as 1, 11 case handled in hand.val()
            case 'A':
                return 1
            case '2':
                return 2
            case '3':
                return 3
            case '4':
                return 4
            case '5':
                return 5
            case '6':
                return 6
            case '7':
                return 7
            case '8':
                return 8
            case '9':
                return 9
            case _:
                return 0

class Shoe:
    # Holds all the cards
    shoe = []
    # Is false when the cut card comes out
    play = True

    def __init__(self, decks=1, shuffle=True, cards=None, penMin=.7, penMax=.8):
        self.play = True
        # If you want to fill with a premade deck (not standard deck), set cards to a specific value when calling
        if cards is not None:
            self.shoe = cards
        else:
            self.shoe = []
        # Otherwise, ignore cards and fill it with standard decks
            for r in ['A','2','3','4','5','6','7','8','9','T','J','Q','K']:
                for s in ['c','d','h','s']:
                    # Puts decks in
                    for x in range(0,decks):
                        c = Card(r,s)
                        self.shoe.append(c)
            # print(str(self))
        # Shuffles the deck by default
        if shuffle:
            random.shuffle(self.shoe)
        l = len(self.shoe)
        # Inserts cut card. When the cut card is drawn, this denotes the last hand
        c = Card('C')
        # Puts it in somewhere between penMin & penMax of the deck.
        # penMax should never be 1, and probably shouldn't be inputted as higher than .9
        # penMin should never be 0, and probably shouldn't be inputted as lower than .5
        self.shoe.insert(random.randrange(int(l*penMin), int(l*penMax)),c)
        # print(str(self))

    # Should work like the standard array toString representation
    def __str__(self):
        return '['+', '.join(str(x) for x in self.shoe)+']'

    def deal(self,fromTop):
        # Deal a card
        c = self.shoe.pop(fromTop)
        # If it's a valid card, return it
        if (c.val() > 0):
            return c
        # If it's not, (cut card), deal the next card
        self.play = False
        return self.deal(fromTop)

class Hand:
    cards = []
    deck = None
    double = False
    hasBeenSplit = False
    isSoft = False
    def __init__(self, cards=None, deck=None, hasBeenSplit=False):
        self.cards = cards if cards is not None else []
        self.deck = deck
        self.double = False
        self.hasBeenSplit = hasBeenSplit
        self.isSoft = False
    # Returns blackjack value of the hand
    def val(self):
        v = 0
        hasAce = False
        self.isSoft = False  # reset each call
        for x in self.cards:
            v += x.val()
            if x.val() == 1:
                hasAce = True
        if hasAce and v <= 11:
            v += 10
            self.isSoft = True
        return v
    # Adds a card to the hand. Use for hit, double, and split
    def addCard(self,fromTop):
        self.cards.append(self.deck.deal(fromTop))
    def __str__(self) -> str:
       return '['+', '.join(str(x) for x in self.cards)+']'

